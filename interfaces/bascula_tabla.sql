CREATE TABLE IF NOT EXISTS peso_slamper(
  pesada_id int unsigned NOT NULL AUTO_INCREMENT PRIMARY KEY,
  articulo VARCHAR(50) DEFAULT 'SIN ASIGNAR',
  peso_total FLOAT (30,15) DEFAULT 000000000000000.000000000000000,
  peso_tara FLOAT (30,15) DEFAULT 000000000000000.000000000000000,
  peso_neto FLOAT (30,15) DEFAULT 000000000000000.000000000000000,
  orden_prod VARCHAR(50) DEFAULT 'SIN ASIGNAR',
  subido_libra BOOLEAN DEFAULT FALSE,
  ejercicio INTEGER UNSIGNED DEFAULT 1900,
  periodo INTEGER UNSIGNED DEFAULT 0, 
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
    ON UPDATE CURRENT_TIMESTAMP
);